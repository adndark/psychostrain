    public void setCrazy() {
        try {
            File file = new File(
                getClass()
                        .getClassLoader()
                        .getResource("resources/fonts/byte.ttf")
                        .getFile()
            );
            Font crazy = Font.createFont(Font.TRUETYPE_FONT, file);
            crazy = crazy.deriveFont(14.0f);
            this.setFont(crazy);
            cancelBtn.setFont(crazy);
            jScrollPane1.setFont(crazy);
            mapList.setFont(crazy);
            mapaslbl.setFont(crazy);
            playBtn.setFont(crazy);
        } catch (FontFormatException ex) {
            System.out.println(ex);
        } catch (IOException ex) {
            System.out.println(ex);
        }
    }

    public void getMaps() {
        DefaultListModel a = new DefaultListModel();
        File dir = new File("personalizedMaps");
        String[] children = dir.list();
        if (children == null) {
            playBtn.setEnabled(false);
        } else {
            for (int i = 0; i < children.length; i++) {
                a.addElement("" + children[i]);
            }
            playBtn.setEnabled(true);
        }
        mapList.setModel(a);
    }

